#include <stdio.h>
#include <stdlib.h>

int main() {
    srand(0x1337);
    int xor_values[] = {
        0x59, 0x38, 0xb, 0x66, 0x19, 0x61, 0x16, 0x6d, 0x6f, 0x75, 0x2e, 0x7e,
        0x17, 0x03, 0x33, 0x6b, 0x27, 0x06, 0x7d, 0x6b, 0x68, 0x0f, 0x60, 0x41,
        0x6c, 0x36, 0x7d, 0x36, 0x73, 0x2a, 0x08, 0x3f, 0x2a, 0x67, 0x58, 0x3b,
        0x46
    };

    int flag_length = sizeof(xor_values) / sizeof(xor_values[0]);
    char flag[flag_length + 1];
    for (int i = 0; i < flag_length; i++) {
        int rand_val = rand() % 100; 
        flag[i] = xor_values[i] ^ rand_val;  
    }
    flag[flag_length] = '\0';
    printf("The flag is: %s\n", flag);
    
    return 0;
}
